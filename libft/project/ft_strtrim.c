#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*trimmed_s;
	int		start;
	int		end;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end]))
		end--;
	trimmed_s = ft_substr(s1, start, end - start + 1);
	return (trimmed_s);
}
